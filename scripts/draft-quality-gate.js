#!/usr/bin/env node
// Dealix Draft Quality Gate (commercial:quality)
// Scores every outreach draft 0-100, applies the Email Gate, builds the Top
// Approval Queue, and writes a daily quality review. AI scores; humans approve.

import path from "node:path";
import {
  ROOT, COMMERCIAL_DIR, REVENUE_DIR, REPORTS_DIR,
  today, loadJson, loadConfig, writeFileEnsured, scoreDraft, statusEmoji,
} from "./commercial-lib.js";

function main() {
  const cfg = loadConfig();
  const queue = loadJson(path.join(REVENUE_DIR, "outreach_queue.json"), { queue: [] });
  const drafts = queue.queue || [];
  const scored = drafts.map((d) => scoreDraft(d, cfg));

  const byStatus = (s) => scored.filter((r) => r.status === s);
  const topPriority = byStatus("top_priority").sort((a, b) => b.total - a.total);
  const approvalQueue = byStatus("approval_queue").sort((a, b) => b.total - a.total);
  const needsRewrite = byStatus("needs_rewrite");
  const rejected = byStatus("rejected");
  const gateFailed = scored.filter((r) => !r.gate_pass);

  // Top 100 Approval Queue: gate pass + score >= 75 + not high risk
  const topQueue = scored
    .filter((r) => r.top_eligible)
    .sort((a, b) => b.total - a.total)
    .slice(0, cfg.rubric.top_queue.capacity);

  const date = today();
  const scoresOut = {
    generated: date,
    distribution_target: cfg.systems.draft_distribution,
    counts: {
      scored: scored.length,
      top_priority: topPriority.length,
      approval_queue: approvalQueue.length,
      needs_rewrite: needsRewrite.length,
      rejected: rejected.length,
      gate_failed: gateFailed.length,
      top_queue_size: topQueue.length,
    },
    top_queue_ids: topQueue.map((r) => r.id),
    drafts: scored,
  };
  writeFileEnsured(path.join(COMMERCIAL_DIR, "draft_scores.json"), JSON.stringify(scoresOut, null, 2) + "\n");

  // ---- markdown review ----
  const row = (r) =>
    `| ${r.id} | ${r.company} | ${r.recommended_system_name || "—"} | ${r.total} | ${statusEmoji(r.status)} ${r.status} | ${r.risk_level} | ${r.gate_pass ? "✅" : "❌"} |`;

  let md = `# Daily Quality Gate Review
*Date: ${date} | Generated by scripts/draft-quality-gate.js*

> AI scores drafts against the rubric. A human approves before anything is sent.
> Scoring rubric: \`company_os/commercial/draft_scoring_rubric.json\`

---

## 1. Summary

| Metric | Value |
|--------|------:|
| Drafts scored | ${scored.length} |
| 🟢 Top priority (85+) | ${topPriority.length} |
| 🟡 Approval queue (75–84) | ${approvalQueue.length} |
| 🟠 Needs rewrite (65–74) | ${needsRewrite.length} |
| 🔴 Rejected (<65) | ${rejected.length} |
| ❌ Email Gate failed | ${gateFailed.length} |
| Top Approval Queue size | ${topQueue.length} / ${cfg.rubric.top_queue.capacity} |

> Daily capacity target is ${cfg.systems.daily_capacity_target.email_drafts} drafts; this run scored ${scored.length}. Targets describe capacity, not a count of verified companies (no purchased lists).

---

## 2. Scoring Thresholds

| Status | Score | Action |
|--------|-------|--------|
| top_priority | 85+ | Send first (after approval) |
| approval_queue | 75–84 | Founder approval queue |
| needs_rewrite | 65–74 | Rewrite, then re-score |
| rejected | < 65 | Do not send |

**Top Approval Queue** = Email Gate pass **AND** score ≥ ${cfg.rubric.top_queue.min_score} **AND** risk_level ≠ high.

---

## 3. All Scored Drafts

| ID | Company | Recommended System | Score | Status | Risk | Gate |
|----|---------|--------------------|------:|--------|------|------|
${scored.map(row).join("\n") || "| — | — | — | — | — | — | — |"}

---

## 4. Top Approval Queue (excludes anything < ${cfg.rubric.top_queue.min_score} or gate-failed)

| Rank | ID | Company | System | Score | Risk |
|-----:|----|---------|--------|------:|------|
${topQueue.map((r, i) => `| ${i + 1} | ${r.id} | ${r.company} | ${r.recommended_system_name} | ${r.total} | ${r.risk_level} |`).join("\n") || "| — | — | — | — | — | — |"}

---

## 5. Email Gate Failures (must fix before send)

${gateFailed.length === 0
      ? "✅ No Email Gate failures in this batch."
      : gateFailed.map((r) => `- **${r.id} ${r.company}** → ${r.gate_fails.join("; ")}`).join("\n")}

---

## 6. Per-Dimension Detail

| ID | Pers/25 | Pain/20 | Fit/20 | CTA/15 | Risk/10 | Tone/10 | Total |
|----|--------:|--------:|-------:|-------:|--------:|--------:|------:|
${scored
      .map((r) => `| ${r.id} | ${r.scores.personalization} | ${r.scores.pain_clarity} | ${r.scores.system_fit} | ${r.scores.cta_clarity} | ${r.scores.risk_safety} | ${r.scores.tone_quality} | ${r.total} |`)
      .join("\n") || "| — | — | — | — | — | — | — | — |"}

---

*Generated: ${date} | Reproduce: \`npm run commercial:quality\` | Machine output: company_os/commercial/draft_scores.json*
`;

  writeFileEnsured(path.join(REPORTS_DIR, "quality", "DAILY_QUALITY_GATE_REVIEW.md"), md);

  console.log(`✅ Quality gate complete.`);
  console.log(`   Scored: ${scored.length} | Top queue: ${topQueue.length} | Gate failed: ${gateFailed.length}`);
  console.log(`   → reports/quality/DAILY_QUALITY_GATE_REVIEW.md`);
  console.log(`   → company_os/commercial/draft_scores.json`);
}

main();
