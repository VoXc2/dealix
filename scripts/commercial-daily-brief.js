#!/usr/bin/env node
// Dealix Founder Daily Super Command (commercial:brief)
// Aggregates the day's factory output into one founder decision sheet.
// Reads generated artifacts; writes reports/founder/DAILY_SUPER_COMMAND.md.

import path from "node:path";
import {
  COMMERCIAL_DIR, REVENUE_DIR, REPORTS_DIR,
  today, loadJson, loadCsv, loadConfig, writeFileEnsured, systemById,
} from "./commercial-lib.js";

function bestSystem(scores, systems) {
  const groups = {};
  for (const d of scores.drafts) {
    if (!d.recommended_system) continue;
    (groups[d.recommended_system] ||= []).push(d.total);
  }
  let best = null;
  for (const [id, totals] of Object.entries(groups)) {
    const avg = totals.reduce((a, b) => a + b, 0) / totals.length;
    if (!best || avg > best.avg) best = { id, avg, count: totals.length };
  }
  if (!best) return null;
  const sys = systemById(systems, best.id);
  return { name: sys ? sys.customer_facing_name_en : best.id, avg: best.avg.toFixed(0), count: best.count };
}

function bestSector(board) {
  const groups = {};
  for (const r of board.board) {
    const g = (groups[r.segment] ||= { count: 0, scoreSum: 0, scored: 0 });
    g.count += 1;
    if (r.score) { g.scoreSum += Number(r.score); g.scored += 1; }
  }
  let best = null;
  for (const [segment, g] of Object.entries(groups)) {
    const avg = g.scored ? g.scoreSum / g.scored : 0;
    if (!best || avg > best.avg) best = { segment, avg, count: g.count };
  }
  return best ? { segment: best.segment, avg: best.avg.toFixed(0), count: best.count } : null;
}

function main() {
  const cfg = loadConfig();
  const date = today();
  const scores = loadJson(path.join(COMMERCIAL_DIR, "draft_scores.json"), { drafts: [], counts: {} });
  const board = loadJson(path.join(COMMERCIAL_DIR, "sales_board.json"), { board: [], counts: {}, call_queue: [], mini_proposal_queue: [] });
  const pipeline = loadJson(path.join(REVENUE_DIR, "pipeline.json"), { counts: {} });
  const proposals = loadJson(path.join(REVENUE_DIR, "proposals.json"), { templates: [], active_proposals: [] });
  const prospects = loadCsv(path.join(REVENUE_DIR, "prospects.csv"));
  const websiteLeads = loadJson(path.join(COMMERCIAL_DIR, "website_leads.json"), { leads: [] });

  const topQueue = scores.drafts.filter((d) => d.top_eligible).sort((a, b) => b.total - a.total);
  const top20 = topQueue.slice(0, 20);
  const calls = (board.call_queue || []).slice(0, 30);
  const sys = bestSystem(scores, cfg.systems);
  const sector = bestSector(board);

  // Critical decision (derived from the day's data)
  let decision;
  if (top20.length > 0) {
    const domSystem = top20[0].recommended_system_name;
    const domSector = sector ? sector.segment : "the strongest sector";
    decision = `Approve the top ${top20.length} ${domSystem} draft(s) targeting ${domSector} — highest pain clarity and lowest delivery complexity in today's batch. Hold everything below score 75.`;
  } else {
    decision = "No drafts cleared the Top Approval Queue today. Decision: rewrite the needs_rewrite drafts and re-run the quality gate before any send.";
  }

  const won = Number(pipeline.counts?.Won || 0);
  const biggestRisk = won === 0
    ? "R001 — No first paid client yet. Concentrate approvals + calls on the highest-fit sector; protect the 5-day delivery SOP."
    : "Delivery capacity vs. concurrent sprints — protect quality with the Delivery Readiness Gate.";

  const starter = (proposals.templates || []).find((t) => t.price_sar) || { price_sar: 2500, name: "Revenue Intelligence Sprint" };

  const md = `# Founder Daily Super Command
*Date: ${date} | Generated by scripts/commercial-daily-brief.js*

> The one sheet that tells you: who to send, who to call, what to approve, what to start, what to watch, and the single decision to make today.
> AI drafts. Human approves. System logs.

---

## 1. Today's Critical Decision

> ${decision}

---

## 2. 400 Draft Factory — Status

| Metric | Value |
|--------|------:|
| Daily capacity target | ${cfg.systems.daily_capacity_target.email_drafts} |
| Drafts scored today | ${scores.counts?.scored ?? scores.drafts.length} |
| 🟢 Top priority (85+) | ${scores.counts?.top_priority ?? 0} |
| 🟡 Approval queue (75–84) | ${scores.counts?.approval_queue ?? 0} |
| 🟠 Needs rewrite | ${scores.counts?.needs_rewrite ?? 0} |
| 🔴 Rejected | ${scores.counts?.rejected ?? 0} |
| ❌ Gate failed | ${scores.counts?.gate_failed ?? 0} |

> Capacity is a target, not a promise of 400 verified companies. Scale comes from real research input only — no purchased lists.

---

## 3. Top Approval Queue — Summary

| Status | Count |
|--------|------:|
| Eligible for Top Queue (gate pass, ≥75, not high-risk) | ${topQueue.length} |
| Capacity | ${cfg.systems.daily_capacity_target.top_approval_queue} |

Full detail: \`reports/quality/DAILY_QUALITY_GATE_REVIEW.md\`

---

## 4. Top 20 Companies to Send (after approval)

| Rank | Company | System | Score |
|-----:|---------|--------|------:|
${top20.map((r, i) => `| ${i + 1} | ${r.company} | ${r.recommended_system_name} | ${r.total} |`).join("\n") || "| — | — | — | — |"}

---

## 5. Top 30 Calls to Make

| # | Company | Priority | System | Next Step |
|--:|---------|----------|--------|-----------|
${calls.map((c, i) => `| ${i + 1} | ${c.company} | ${c.priority} | ${c.recommended_system} | ${c.next_step} |`).join("\n") || "| — | — | — | — | — |"}

Full briefs: \`reports/sales_ops/SALES_OPS_BOARD_STATUS.md\`

---

## 6. Mini Proposals Waiting Approval

${(board.mini_proposal_queue || []).length
      ? board.mini_proposal_queue.map((m) => `- **${m.company}** → ${m.recommended_system} (${m.approval_status})`).join("\n")
      : "_0 waiting — no positive signal returned yet. A Mini Proposal is generated only on a positive reply / successful call._"}

---

## 7. Delivery Pipelines — Status

| Stage | Count |
|-------|------:|
| Won (to start) | ${pipeline.counts?.Won ?? 0} |
| In Delivery | ${pipeline.counts?.Delivery ?? 0} |
| Retainer (active) | ${pipeline.counts?.Retainer ?? 0} |

> No delivery starts until the Delivery Readiness Gate passes (\`docs/quality/DELIVERY_READINESS_GATE_AR.md\`).

---

## 8. Website Leads

${(websiteLeads.leads || []).length
      ? websiteLeads.leads.map((l) => `- ${l.company || "Lead"} — ${l.interest || "—"}`).join("\n")
      : "_0 captured. No intake submissions recorded in company_os/commercial/website_leads.json yet._"}

---

## 9. Best Performing System

${sys ? `**${sys.name}** — avg draft score ${sys.avg} across ${sys.count} draft(s).` : "_Not enough scored drafts to rank systems._"}

---

## 10. Best Sector

${sector ? `**${sector.segment}** — ${sector.count} companies on the board, avg draft score ${sector.avg}.` : "_Not enough data to rank sectors._"}

---

## 11. Biggest Risk

> ${biggestRisk}

---

## 12. Cash / Pricing Opportunities

- Starter offer: **${starter.name}** at **${starter.price_sar} SAR** (intro). ${prospects.length} researched prospects in pipeline.
- Retainer ladder: ${(proposals.templates || []).filter((t) => t.price_sar_monthly).map((t) => `${t.price_sar_monthly} SAR/mo`).join(" · ") || "—"}.
- All pricing requires founder approval (no AI pricing decisions).

---

## 13. Tomorrow's Recommendation

1. Approve today's Top Queue and send (human-in-the-loop).
2. Run the ${calls.length} due call(s); log outcomes back to the board.
3. Generate Mini Proposals for any positive reply (pending approval).
4. Refresh research input to grow tomorrow's batch toward the ${cfg.systems.daily_capacity_target.email_drafts} target — real sources only.

---

*Generated: ${date} | Reproduce: \`npm run commercial:brief\` | Inputs: draft_scores.json, sales_board.json, pipeline.json*
`;

  writeFileEnsured(path.join(REPORTS_DIR, "founder", "DAILY_SUPER_COMMAND.md"), md);

  console.log(`✅ Daily Super Command complete.`);
  console.log(`   Top20: ${top20.length} | Calls: ${calls.length} | Best system: ${sys ? sys.name : "n/a"}`);
  console.log(`   → reports/founder/DAILY_SUPER_COMMAND.md`);
}

main();
