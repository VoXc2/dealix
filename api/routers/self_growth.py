router = APIRouter(prefix="/api/v1/self-growth", tags=["self-growth"])

REPO_ROOT = Path(__file__).resolve().parents[2]
MATRIX_JSON = REPO_ROOT / "landing" / "assets" / "data" / "service-readiness.json"
SEO_REPORT = REPO_ROOT / "docs" / "SEO_AUDIT_REPORT.json"


def _load_json(path: Path) -> dict:
    if not path.exists():
        raise HTTPException(
            status_code=503,
            detail=(
                f"{path.name} not generated yet. "
                "Run scripts/export_service_readiness_json.py and "
                "scripts/seo_audit.py."
            ),
        )
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        raise HTTPException(
            status_code=500,
            detail=f"{path.name} is not valid JSON: {exc.msg}",
        ) from exc


