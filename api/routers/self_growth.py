@router.post("/publishing/check")
async def publishing_check(payload: dict = Body(...)) -> dict:
    """Run the safe-publishing gate over a draft string.

    Body: ``{"text": "<the draft to check>", "language": "ar"|"en"|"bilingual"}``
    Returns the typed ``SafePublishingResult`` — never stores the
    text; the caller owns persistence (typically via ApprovalGate).
    """
    text = payload.get("text")
    if not isinstance(text, str) or not text.strip():
        raise HTTPException(
            status_code=400,
            detail="payload.text must be a non-empty string",
        )
    language_raw = str(payload.get("language", "ar")).lower()
    try:
        from auto_client_acquisition.self_growth_os.schemas import Language
        language = Language(language_raw)
    except ValueError:
        raise HTTPException(
            status_code=400,
            detail=f"language must be one of ar/en/bilingual; got {language_raw!r}",
        ) from None

    result = safe_publishing_gate.check_text(text, language=language)
    return result.model_dump(mode="json")
